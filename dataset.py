"""Build the labeled eval dataset.

Sources (config dataset.source):
  - "corpus"      : draw faithful pairs from data/policies_faithful.jsonl
                    (generated by generate_pairs.py); auto-generates it if missing
                    or too small. This is the production-scale path.
  - "seed_pairs"  : draw from the small hand-written data/seed_pairs.jsonl (demo).

Faithful pairs are partitioned disjointly from the pairs that get corrupted, so no
policy appears both faithful and unfaithful (cleaner statistics). Each unfaithful
example applies one tagged corruption (or two, for "combined"). Per-type counts are
balanced greedily so detection rate is measurable per type. "subtle" variants are
single-field micro-drifts (hard negatives). Faithful pairs carry equivalence_hard so
false-reject rate can be reported on non-literal-but-equivalent phrasings separately.

Eval row schema:
  {id, english, json, gold_verdict, gold_mismatches[], corruption_type,
   hard_negative, equivalence_hard, source_id}
"""
import argparse
import copy
import random

from utils import load_config, abspath, read_jsonl, write_jsonl

CURRENCY_SWAP = {"INR": "USD", "USD": "EUR", "EUR": "INR"}
RESTRICTIVE_WINDOW = ("09:00", "17:00")


def _mm(field, issue):
    return {"field": field, "issue": issue}


# ---- single corruptions: (json, subtle) -> (corrupted_json, mismatch) | None ----
def c_wrong_amount(j, subtle):
    j = copy.deepcopy(j); old = j["max_amount"]
    j["max_amount"] = round(old * 1.10) if subtle else old * 2
    return j, _mm("max_amount", f"amount {j['max_amount']} != policy {old}")


def c_dropped_category(j, subtle):
    if not isinstance(j.get("categories"), list) or len(j["categories"]) < 2:
        return None
    j = copy.deepcopy(j); dropped = j["categories"].pop()
    return j, _mm("categories", f"category '{dropped}' missing")


def c_added_merchant(j, subtle):
    j = copy.deepcopy(j); extra = "QuickMart"
    if j.get("merchants") == "any":
        j["merchants"] = [extra]
        return j, _mm("merchants", "narrowed 'any' to an unstated merchant list")
    if isinstance(j.get("merchants"), list):
        j["merchants"] = j["merchants"] + [extra]
        return j, _mm("merchants", f"merchant '{extra}' added, not in policy")
    return None


def c_dropped_merchant(j, subtle):
    if not isinstance(j.get("merchants"), list) or len(j["merchants"]) < 2:
        return None
    j = copy.deepcopy(j); dropped = j["merchants"].pop()
    return j, _mm("merchants", f"merchant '{dropped}' dropped")


def c_overpermissive_merchants(j, subtle):
    if not isinstance(j.get("merchants"), list):
        return None
    j = copy.deepcopy(j); old = j["merchants"]
    j["merchants"] = "any"
    return j, _mm("merchants", f"widened {old} to 'any' (over-permissive)")


def c_widened_time_window(j, subtle):
    if "time_window" not in j:
        return None
    j = copy.deepcopy(j); end = j["time_window"]["end"]
    try:
        hh, mm = [int(x) for x in end.split(":")]
    except Exception:
        return None
    delta = 30 if subtle else 180
    tot = min(hh * 60 + mm + delta, 23 * 60 + 59)
    j["time_window"]["end"] = f"{tot // 60:02d}:{tot % 60:02d}"
    if j["time_window"]["end"] == end:
        return None
    return j, _mm("time_window", f"end widened to {j['time_window']['end']} from {end}")


def c_narrowed_time_window(j, subtle):
    if "time_window" not in j:
        return None
    j = copy.deepcopy(j); tw = j["time_window"]; old = (tw["start"], tw["end"])
    if subtle:
        try:
            hh, mm = [int(x) for x in tw["end"].split(":")]
        except Exception:
            return None
        tot = max(hh * 60 + mm - 30, 0)
        tw["end"] = f"{tot // 60:02d}:{tot % 60:02d}"
    else:
        tw["start"], tw["end"] = RESTRICTIVE_WINDOW
    if (tw["start"], tw["end"]) == old:
        return None
    return j, _mm("time_window", f"narrowed to {tw['start']}-{tw['end']} from {old[0]}-{old[1]}")


def c_swapped_currency(j, subtle):
    j = copy.deepcopy(j); old = j.get("currency", "INR")
    j["currency"] = CURRENCY_SWAP.get(old, "USD")
    return j, _mm("currency", f"currency {j['currency']} != policy {old}")


def c_wrong_period(j, subtle):
    j = copy.deepcopy(j); old = j.get("period", "daily")
    order = ["per_transaction", "daily", "weekly", "monthly"]
    others = [p for p in order if p != old]
    j["period"] = others[0] if subtle else others[-1]
    return j, _mm("period", f"period {j['period']} != policy {old}")


CORRUPTORS = {
    "wrong_amount": c_wrong_amount,
    "dropped_category": c_dropped_category,
    "added_merchant": c_added_merchant,
    "dropped_merchant": c_dropped_merchant,
    "overpermissive_merchants": c_overpermissive_merchants,
    "widened_time_window": c_widened_time_window,
    "narrowed_time_window": c_narrowed_time_window,
    "swapped_currency": c_swapped_currency,
    "wrong_period": c_wrong_period,
}


def _apply_combined(base_json, single_types, rng):
    """Apply two distinct single corruptions; return (json, [mm, mm]) or None."""
    types = single_types[:]
    rng.shuffle(types)
    cur = copy.deepcopy(base_json)
    mms, used = [], 0
    for t in types:
        res = CORRUPTORS[t](cur, rng.random() < 0.5)
        if res is None:
            continue
        cur, mm = res
        mms.append(mm); used += 1
        if used == 2:
            break
    return (cur, mms) if used == 2 else None


# ----------------------------------------------------------------- source pairs
def _load_source(cfg):
    dcfg = cfg["dataset"]
    source = dcfg.get("source", "corpus")
    if source == "seed_pairs":
        pairs = read_jsonl(abspath(cfg["paths"]["seed_pairs"]))
        for p in pairs:
            p.setdefault("equivalence_hard", False)
        return pairs

    corpus_path = abspath(cfg["paths"]["policy_corpus"])
    needed = int(dcfg.get("target_size", 2000) * 1.1)
    language = dcfg.get("language", "hinglish")
    pairs = read_jsonl(corpus_path) if corpus_path.exists() else []
    # regenerate if too small OR the corpus language doesn't match the requested one
    lang_ok = (not pairs) or all(
        (p.get("lang", "english") == language) or language == "mixed" for p in pairs[:50])
    if len(pairs) < needed or not lang_ok:
        from generate_pairs import generate
        print(f"[dataset] (re)generating corpus: have {len(pairs)}, need {needed}, "
              f"language={language}")
        pairs = generate(needed, cfg["seed"], language)
        write_jsonl(corpus_path, pairs)
        print(f"[dataset] wrote {corpus_path}")
    return pairs


# ----------------------------------------------------------------------- build
def synthesize(cfg):
    dcfg = cfg["dataset"]
    rng = random.Random(cfg["seed"])
    pairs = _load_source(cfg)
    rng.shuffle(pairs)

    target = int(dcfg.get("target_size", len(pairs)))
    f = dcfg["faithful_fraction"]
    n_faith = max(1, round(target * f))
    n_unf = max(0, target - n_faith)

    # disjoint partition
    faith_pairs = pairs[:n_faith]
    src = pairs[n_faith:] or pairs            # fall back to reuse if corpus small
    if not faith_pairs:
        faith_pairs = pairs

    rows = []
    for i, p in enumerate(faith_pairs):
        rows.append({
            "id": f"f{i}", "english": p["english"], "json": copy.deepcopy(p["json"]),
            "gold_verdict": "FAITHFUL", "gold_mismatches": [],
            "corruption_type": "none", "hard_negative": False,
            "equivalence_hard": bool(p.get("equivalence_hard", False)),
            "lang": p.get("lang", "english"),
            "source_id": p.get("policy_id", i),
        })

    enabled = list(dcfg["corruptions"])
    single_types = [t for t in enabled if t != "combined" and t in CORRUPTORS]
    use_combined = "combined" in enabled
    subtle_variants = [False, True] if dcfg.get("include_subtle", True) else [False]

    counts = {t: 0 for t in enabled}
    ui = 0          # index into src
    attempts = 0
    while len([r for r in rows if r["gold_verdict"] == "NOT_FAITHFUL"]) < n_unf and attempts < n_unf * 50:
        attempts += 1
        t = min(counts, key=lambda k: counts[k])          # greedy balance
        p = src[ui % len(src)]; ui += 1
        base = p["json"]
        subtle = rng.choice(subtle_variants)
        if t == "combined":
            res = _apply_combined(base, single_types, rng)
            if res is None:
                continue
            cj, mms = res
            ctype, hard = "combined", True
        else:
            r = CORRUPTORS[t](base, subtle)
            if r is None:
                continue
            cj, mm = r
            mms, ctype, hard = [mm], t, bool(subtle)
        counts[t] += 1
        idx = counts[t]
        rows.append({
            "id": f"u-{ctype}-{idx}", "english": p["english"], "json": cj,
            "gold_verdict": "NOT_FAITHFUL", "gold_mismatches": mms,
            "corruption_type": ctype, "hard_negative": hard,
            "equivalence_hard": False, "lang": p.get("lang", "english"),
            "source_id": p.get("policy_id", ui),
        })

    rng.shuffle(rows)
    n = len(rows)
    nf = sum(r["gold_verdict"] == "FAITHFUL" for r in rows)
    eh = sum(r["equivalence_hard"] for r in rows)
    per_type = {}
    for r in rows:
        per_type[r["corruption_type"]] = per_type.get(r["corruption_type"], 0) + 1
    print(f"[dataset] {n} examples | faithful {nf} ({nf/n:.0%}, of which "
          f"{eh} equivalence-hard) | unfaithful {n-nf}")
    print(f"[dataset] per corruption type: {per_type}")
    _power_note(n - nf)
    return rows


def _power_note(n_unf):
    """Rough 95% CI half-width on a rate estimated from n_unf trials (worst case p=0.5)."""
    if n_unf <= 0:
        return
    import math
    hw = 1.96 * math.sqrt(0.25 / n_unf)
    print(f"[dataset] ~{n_unf} unfaithful -> false-accept rate measurable to "
          f"+/-{hw*100:.1f}% (95% CI, worst case). Lower for small true rates.")


def load_eval(cfg):
    eval_path = abspath(cfg["paths"]["eval_dataset"])
    if cfg["dataset"].get("synthesize", True):
        rows = synthesize(cfg)
        write_jsonl(eval_path, rows)
        print(f"[dataset] wrote {eval_path}")
        return rows
    if not eval_path.exists():
        raise FileNotFoundError(
            f"{eval_path} missing and dataset.synthesize is false.")
    rows = read_jsonl(eval_path)
    for r in rows:
        r.setdefault("equivalence_hard", False)
    print(f"[dataset] loaded {len(rows)} labeled examples from {eval_path}")
    return rows


if __name__ == "__main__":
    ap = argparse.ArgumentParser(description="Build/refresh the eval dataset.")
    ap.add_argument("--config", default="config.yaml")
    args = ap.parse_args()
    load_eval(load_config(args.config))
